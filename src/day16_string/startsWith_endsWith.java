package day16_string;

public class startsWith_endsWith {
    public static void main(String[] args) {
        String a = "Ertur";
       //startsWith
        System.out.println(a.startsWith("E")); // true
        System.out.println(a.startsWith("M"));//false
        System.out.println(a.startsWith("Er"));//true can use multi char
        System.out.println(a.startsWith("e"));//false case sensitive





    }
}
