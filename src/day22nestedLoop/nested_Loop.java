package day22nestedLoop;

public class nested_Loop {
    public static void main(String[] args) {
        for (int i = 1; i <=5; i++){

            for (int a = 1; a <=5; a++){
                System.out.print(a+" ");
            }
            System.out.println();
        }
        System.out.println("======================================");
        for (int b = 1; b <= 10; b++){
            for (int c = 1; c<= 5; c++){
                System.out.print("*");
            }
            System.out.println();
        }







    }
}
