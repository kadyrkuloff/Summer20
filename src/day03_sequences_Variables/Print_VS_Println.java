package day03_sequences_Variables;

public class Print_VS_Println {
    public static void main(String[] args) {
        System.out.print("a");
        System.out.print("b");
        System.out.print("c");
        System.out.println();
        System.out.println("a"); //a + new line
        System.out.println("b"); //b + new line
        System.out.println("c"); //c + new line


    }
}
