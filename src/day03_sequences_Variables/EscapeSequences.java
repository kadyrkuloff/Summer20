package day03_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone \n\tMy Name is Cybertek"); // \n - new line; \t - tab;
        System.out.println("\t\tI am in Virginia\n");
        System.out.println("\n\n\t\t\tI Love Programming");

        System.out.println("\\"); //this prints me one back slash
        System.out.println("/"); //this prints me one front slash
        System.out.println("\\\\"); // this prints me two back slash
        System.out.println("//"); // this prints me two front slash
        System.out.println("My name is \'Ertur\'"); //appends single quotation mark
        System.out.println("My name is 'Ertur'"); //single quot can be append without escapesqnce
        System.out.println("My favourite TV-series: \"Game of Thrones\""); // \" - only way to print double "






    }





}
