package day21loops;

public class doWhile {
    public static void main(String[] args) {
        boolean result = false;
        while (result){
            System.out.println("Hello World");
        }
        System.out.println("++++++++=========+++++++++");
        do{//just do it
            System.out.println("Hello World");
        }while(result);




    }
}
