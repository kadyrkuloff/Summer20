package day21loops;

public class do_while_prac {
    public static void main(String[] args) {
        int num = 1;



        do{
            System.out.print(num + " ");
          num++;
        }while(num<=20);
        System.out.println();
        char ch = 'Z';
        do{
            System.out.print(ch+" ");
            ch--;
        }while(ch>='A');


    }
}
